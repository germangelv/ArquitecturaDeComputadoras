	
		ORG	PRIM
PRIM		EQU	$0001
ULTI		EQU	$0008

VECTOR		RMB	ULTI-PRIM+1

		ORG	$0010
MAYUSCULAS	RMB	ULTI-PRIM+1

		ORG	$0020
MINUSCULAS	RMB	ULTI-PRIM+1

		ORG	$0030
NUMEROS		RMB 	ULTI-PRIM+1
PMAY		RMB	2
PMIN		RMB	2
PNUM		RMB	2

		ORG	$8000
Inicio		LDY	#MINUSCULAS
		STY	PMIN
		LDY	#MAYUSCULAS		;CARGO LA DIRECCION DEL PRINCIPIO EN LOS RESPECTIVOS PUNTEROS
		STY	PMAY
		LDY	#NUMEROS
		STY	PNUM
		
		LDX	#VECTOR
SIGUE		LDAA	0,X
		CMPA	#'9'
		BHI	MIN_MAY
		CMPA	#'0'
		BLO	NO_VALID
		LDY	PNUM
		STAA	0,Y
		INC	PNUM+1
NO_VALID
INCREM		INX
		CPX	#ULTI
		BLS	SIGUE
FIN		BRA	FIN

MIN_MAY		CMPA	#'Z'
		BHI	ES_MIN
		CMPA	#'A'
		BLO	NO_VALID
		LDY	PMAY
		STAA	0,Y
		INC	PMAY+1
		BRA	INCREM

ES_MIN		CMPA	#'a'
		BLO	NO_VALID
		CMPA	#'z'
		BHI	NO_VALID
		LDY	PMIN
		STAA	0,Y
		INC	PMIN+1
		BRA	INCREM
		

	ORG	PRIM
	db	$61,'(','X','#','1','Q',$54,'+'

	ORG $FFFE
	dw Inicio	